// Choose language
function chooseLanguage(cb) {
  $('.langs .en').click(function(e) {
    if (window.lang != 'en') {
      window.lang = 'en';
      cb();
    }
  });

  $('.langs .vi').click(function(e) {
    if (window.lang != 'vi') {
      window.lang = 'vi';
      cb();
    }
  });
}

